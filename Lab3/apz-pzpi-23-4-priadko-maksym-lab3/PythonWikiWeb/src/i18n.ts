const catalogs: Record<LocaleCode, Messages> = {
  "uk-UA": uk,
  "en-US": en
};

export function t(locale: LocaleCode, key: string): string {
  return catalogs[locale][key] ?? en[key] ?? key;
}

export function formatDate(locale: LocaleCode, value: string | Date): string {
  return new Intl.DateTimeFormat(locale, {
    dateStyle: "medium",
    timeStyle: "short"
  }).format(new Date(value));
}

export function sortText<T>(locale: LocaleCode, items: T[], pick: (item: T) => string): T[] {
  const collator = new Intl.Collator(locale, { sensitivity: "base", numeric: true });
  return [...items].sort((a, b) => collator.compare(pick(a), pick(b)));
}
